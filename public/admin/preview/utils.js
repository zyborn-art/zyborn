  /**
   * Get SVG icon markup by name
   * @param {string} name - Icon name (e.g., 'document', 'folder', 'globe')
   * @param {object} options - Optional: { size: '24', class: 'my-class' }
   * @returns {string} SVG HTML string
   */
  function getIconSvg(name, options) {
    options = options || {};
    var svg = SVG_ICONS[name];
    
    if (!svg) {
      // Fallback: return the name as text (for emoji fallback)
      return '<span class="icon-fallback">' + escapeHtml(name) + '</span>';
    }
    
    // Add wrapper with optional size and class
    var size = options.size || '24';
    var className = options.class || '';
    var wrapper = '<span class="icon' + (className ? ' ' + className : '') + '" style="width: ' + size + 'px; height: ' + size + 'px; display: inline-flex; align-items: center; justify-content: center;">';
    
    return wrapper + svg + '</span>';
  }

